function countTotalBones(poseResult: PoseResult): number {
  return poseResult.components.reduce(
    (sum, c) => sum + c.skeleton.bones.length,
    0,
  );
}

// ---------------------------------------------------------------------------
// Component encoding orchestration
// ---------------------------------------------------------------------------

function encodeComponent(
  ctx: EncodingContext,
  segComponent: SegComponent,
  poseComponent: PoseComponent | undefined,
  artboardWidth: number,
  artboardHeight: number,
  animPlan: ComponentAnimationPlan | undefined,
): void {
  const localState: ArtboardLocalState = {
    nextId: 1,
    boneIdMap: new Map(),
  };

  const artboard = encodeArtboard(
    ctx,
    segComponent,
    artboardWidth,
    artboardHeight,
  );
  ctx.objects.push(artboard);

  const artboardLocalId = 0;
  const imageLocalId = allocateArtboardLocalId(localState);
  const image = encodeImage(ctx, segComponent, artboardLocalId);
  ctx.objects.push(image);

  if (segComponent.mesh && segComponent.mesh.vertices.length > 0) {
    const mesh = segComponent.mesh;
    const meshLocalId = allocateArtboardLocalId(localState);
    const meshObj = encodeMesh(ctx, mesh, mesh.vertices.length, imageLocalId);
    ctx.objects.push(meshObj);

    for (const vertex of mesh.vertices) {
      allocateArtboardLocalId(localState);
      const meshVertex = encodeMeshVertex(ctx, vertex, meshLocalId);
      ctx.objects.push(meshVertex);
    }

    allocateArtboardLocalId(localState);
    const skin = encodeSkin(ctx, meshLocalId);
    ctx.objects.push(skin);
  }

  if (poseComponent) {
    const rootBones: PoseBone[] = [];
    const childBones: PoseBone[] = [];

    for (const bone of poseComponent.skeleton.bones) {
      if (bone.parent === null) {
        rootBones.push(bone);
      } else {
        childBones.push(bone);
      }
    }

    for (const bone of rootBones) {
      const rootBoneLocalId = allocateArtboardLocalId(localState);
      const rootBone = encodeRootBone(ctx, bone, artboardLocalId);
      ctx.objects.push(rootBone);
      localState.boneIdMap.set(bone.name, rootBoneLocalId);
    }

    let remaining = [...childBones];
    let lastCount = -1;
    while (remaining.length > 0 && remaining.length !== lastCount) {
      lastCount = remaining.length;
      const stillRemaining: PoseBone[] = [];

      for (const bone of remaining) {
        const parentId = localState.boneIdMap.get(bone.parent!);
        if (parentId !== undefined) {
          const boneLocalId = allocateArtboardLocalId(localState);
          const boneObj = encodeBone(ctx, bone, parentId);
          ctx.objects.push(boneObj);
          localState.boneIdMap.set(bone.name, boneLocalId);
        } else {
          stillRemaining.push(bone);
        }
      }

      remaining = stillRemaining;
    }

    if (remaining.length > 0) {
      ctx.warnings.push(
        `Component ${segComponent.id}: ${remaining.length} bones could not be encoded ` +
          `due to missing parent references: ${remaining.map((b) => b.name).join(', ')}`,
      );
    }

    if (animPlan) {
      encodeAnimation(ctx, segComponent.id, animPlan, localState);
    }
  }
}

