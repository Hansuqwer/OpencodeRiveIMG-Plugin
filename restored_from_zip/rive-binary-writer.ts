/**
 * rive-binary-writer.ts
 *
 * Isolated `.riv` binary file writer for the image-to-rive pipeline.
 *
 * Module boundaries (enforced by convention):
 *   - Imports byte utilities from: rive-writer.ts
 *   - Imports format constants from: rive-format-defs.ts
 *   - Type-only imports from: pipeline/contracts.ts (pure interfaces, no logic)
 *   - No imports from pipeline stage modules, runtime helpers, or CLI
 *   - No shared mutable state with the .rivebundle export path
 *
 * Status: Phase 3 (Animation — Gate 1 complete, Gate 2 in progress).
 *   - Public API is defined and stable.
 *   - writeRiveFile() writes a complete object graph with animations.
 *   - Generated bytes are theoretically loadable by Rive runtime but not yet
 *     verified by an automated harness (Gate 2).
 *
 * Phase roadmap:
 *   Phase 1 (complete): API isolation + header stub
 *   Phase 2 (complete): Static content — Backboard, Artboard, Image, Mesh, MeshVertex, Bone, Skin
 *   Phase 3 (this file): Animation — LinearAnimation, KeyedObject, KeyedProperty, KeyFrameDouble
 *   Phase 4: Verification harness — runtime load testing via Rive WASM
 */

