function encodeBackboard(ctx: EncodingContext): EncodedObject {
  const w = new BinaryWriter();
  const id = ctx.nextId++;

  // Backboard has no properties in the minimal case
  // (It inherits from nothing and has no required fields)

  const bytes = w.toUint8Array();
  const propertyKeys = new Set<number>();

  return { typeKey: RiveTypeKey.Backboard, id, bytes, propertyKeys };
}

function encodeArtboard(
  ctx: EncodingContext,
  component: SegComponent,
  width: number,
  height: number,
): EncodedObject {
  const w = new BinaryWriter();
  const id = ctx.nextId++;

  // LayoutComponent properties (inherited)
  writePropertyValue(w, RivePropertyKey.width, width);
  writePropertyValue(w, RivePropertyKey.height, height);
  writePropertyValue(w, RivePropertyKey.layout_clip, 0); // No clipping

  // Artboard-specific properties
  writePropertyValue(w, RivePropertyKey.artboard_originX, width / 2);
  writePropertyValue(w, RivePropertyKey.artboard_originY, height / 2);

  // Component properties
  writePropertyValue(w, RivePropertyKey.name, component.label || component.id);
  writePropertyValue(w, RivePropertyKey.parentId, 0); // Parent is Backboard (id 0)

  const bytes = w.toUint8Array();
  const propertyKeys = new Set<number>([
    RivePropertyKey.width.key,
    RivePropertyKey.height.key,
    RivePropertyKey.layout_clip.key,
    RivePropertyKey.artboard_originX.key,
    RivePropertyKey.artboard_originY.key,
    RivePropertyKey.name.key,
    RivePropertyKey.parentId.key,
  ]);

  return { typeKey: RiveTypeKey.Artboard, id, bytes, propertyKeys };
}

function encodeImage(
  ctx: EncodingContext,
  component: SegComponent,
  parentId: number,
): EncodedObject {
  const w = new BinaryWriter();
  const id = ctx.nextId++;

  // Image properties
  writePropertyValue(
    w,
    RivePropertyKey.image_originX,
    component.image_size.w / 2,
  );
  writePropertyValue(
    w,
    RivePropertyKey.image_originY,
    component.image_size.h / 2,
  );

  // Component properties (Drawable -> Node -> TransformComponent -> ... -> Component)
  writePropertyValue(
    w,
    RivePropertyKey.name,
    `${component.label || 'image'}_instance`,
  );
  writePropertyValue(w, RivePropertyKey.parentId, parentId);

  // Drawable blend mode
  writePropertyValue(w, RivePropertyKey.blendModeValue, 3); // SrcOver

  // WorldTransformComponent opacity
  writePropertyValue(w, RivePropertyKey.opacity, 1.0);

  // TransformComponent properties
  writePropertyValue(w, RivePropertyKey.rotation, 0);
  writePropertyValue(w, RivePropertyKey.scaleX, 1);
  writePropertyValue(w, RivePropertyKey.scaleY, 1);

  // Node position
  writePropertyValue(w, RivePropertyKey.node_x, component.image_size.w / 2);
  writePropertyValue(w, RivePropertyKey.node_y, component.image_size.h / 2);

  const bytes = w.toUint8Array();
  const propertyKeys = new Set<number>([
    RivePropertyKey.image_originX.key,
    RivePropertyKey.image_originY.key,
    RivePropertyKey.name.key,
    RivePropertyKey.parentId.key,
    RivePropertyKey.blendModeValue.key,
    RivePropertyKey.opacity.key,
    RivePropertyKey.rotation.key,
    RivePropertyKey.scaleX.key,
    RivePropertyKey.scaleY.key,
    RivePropertyKey.node_x.key,
    RivePropertyKey.node_y.key,
  ]);

  return { typeKey: RiveTypeKey.Image, id, bytes, propertyKeys };
}

