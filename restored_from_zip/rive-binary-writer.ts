function encodeTriangleIndexBytes(
  mesh: SegMesh,
  vertexCount: number,
): Uint8Array {
  const writer = new BinaryWriter();
  const maxVertexIndex = vertexCount - 1;

  for (const tri of mesh.triangles) {
    for (const index of tri) {
      if (!Number.isInteger(index) || index < 0 || index > maxVertexIndex) {
        throw new Error(
          `Mesh triangle index ${index} is out of bounds for ${vertexCount} vertices.`,
        );
      }
      writer.writeVarint(index);
    }
  }

  return writer.toUint8Array();
}

function encodeMesh(
  ctx: EncodingContext,
  mesh: SegMesh,
  vertexCount: number,
  parentId: number,
): EncodedObject {
  const w = new BinaryWriter();
  const id = ctx.nextId++;

  const triangleBytes = encodeTriangleIndexBytes(mesh, vertexCount);
  writePropertyValue(w, RivePropertyKey.triangleIndexBytes, triangleBytes);

  // Component properties
  writePropertyValue(w, RivePropertyKey.name, 'mesh');
  writePropertyValue(w, RivePropertyKey.parentId, parentId);

  const bytes = w.toUint8Array();
  const propertyKeys = new Set<number>([
    RivePropertyKey.triangleIndexBytes.key,
    RivePropertyKey.name.key,
    RivePropertyKey.parentId.key,
  ]);

  return { typeKey: RiveTypeKey.Mesh, id, bytes, propertyKeys };
}

function encodeBone(
  ctx: EncodingContext,
  bone: PoseBone,
  parentId: number,
): EncodedObject {
  const w = new BinaryWriter();
  const id = ctx.nextId++;

  // Bone is not a RootBone, so use regular Bone encoding
  // SkeletalComponent -> TransformComponent -> WorldTransformComponent -> ContainerComponent -> Component

  // Component properties
  writePropertyValue(w, RivePropertyKey.name, bone.name);
  writePropertyValue(w, RivePropertyKey.parentId, parentId);

  // TransformComponent properties
  writePropertyValue(w, RivePropertyKey.rotation, bone.rotation);
  writePropertyValue(w, RivePropertyKey.scaleX, 1);
  writePropertyValue(w, RivePropertyKey.scaleY, 1);

  // WorldTransformComponent opacity
  writePropertyValue(w, RivePropertyKey.opacity, 1.0);

  // Bone-specific properties
  writePropertyValue(w, RivePropertyKey.bone_length, bone.length);

  // Note: Bone position is determined by parent connection in Rive
  // The actual x/y of the bone tip would be calculated from rotation and length

  const bytes = w.toUint8Array();
  const propertyKeys = new Set<number>([
    RivePropertyKey.name.key,
    RivePropertyKey.parentId.key,
    RivePropertyKey.rotation.key,
    RivePropertyKey.scaleX.key,
    RivePropertyKey.scaleY.key,
    RivePropertyKey.opacity.key,
    RivePropertyKey.bone_length.key,
  ]);

  return { typeKey: RiveTypeKey.Bone, id, bytes, propertyKeys };
}

