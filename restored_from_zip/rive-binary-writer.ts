function encodeKeyFrameDouble(
  ctx: EncodingContext,
  frame: number,
  value: number,
  interpolationType: number,
  interpolatorId: number | null,
): EncodedObject {
  const w = new BinaryWriter();
  const id = ctx.nextId++;

  // KeyFrame properties (from KeyFrame parent)
  writePropertyValue(w, RivePropertyKey.frame, frame);

  // InterpolatingKeyFrame properties
  writePropertyValue(w, RivePropertyKey.interpolationType, interpolationType);
  if (interpolatorId !== null) {
    writePropertyValue(w, RivePropertyKey.interpolatorId, interpolatorId);
  }

  // KeyFrameDouble properties
  writePropertyValue(w, RivePropertyKey.keyFrameDouble_value, value);

  const bytes = w.toUint8Array();
  const propertyKeys = new Set<number>([
    RivePropertyKey.frame.key,
    RivePropertyKey.interpolationType.key,
    ...(interpolatorId !== null ? [RivePropertyKey.interpolatorId.key] : []),
    RivePropertyKey.keyFrameDouble_value.key,
  ]);

  return { typeKey: RiveTypeKey.KeyFrameDouble, id, bytes, propertyKeys };
}

