function writeFileHeader(w: BinaryWriter, fileId = 0): void {
  w.writeBytes(RIVE_MAGIC);
  w.writeVarint(RIVE_MAJOR_VERSION);
  w.writeVarint(RIVE_MINOR_VERSION);
  w.writeVarint(fileId);
}

