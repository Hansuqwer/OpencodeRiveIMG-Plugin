function writePropertyValue(
  w: BinaryWriter,
  prop: RivePropertyDef,
  value: number | string | boolean | Uint8Array,
): void {
  w.writeVarint(prop.key);

  switch (prop.backingType) {
    case TocBackingType.Uint:
      w.writeVarint(
        typeof value === 'boolean'
          ? value
            ? 1
            : 0
          : typeof value === 'number'
            ? value
            : Number(value),
      );
      break;
    case TocBackingType.String:
      if (value instanceof Uint8Array) {
        w.writeVarint(value.length);
        w.writeBytes(value);
      } else {
        w.writeString(String(value));
      }
      break;
    case TocBackingType.Float:
      w.writeFloat32(Number(value));
      break;
    case TocBackingType.Color:
      w.writeUint32(Number(value));
      break;
    default:
      throw new Error(`Unknown backing type: ${prop.backingType}`);
  }
}

function allocateArtboardLocalId(state: ArtboardLocalState): number {
  const id = state.nextId;
  state.nextId += 1;
  return id;
}

