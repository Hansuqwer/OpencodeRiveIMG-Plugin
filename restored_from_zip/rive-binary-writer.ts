/**
 * Encode animations from a component animation plan.
 * Creates: LinearAnimation → KeyedObject → KeyedProperty → KeyFrameDouble for each animation
 */
function encodeAnimation(
  ctx: EncodingContext,
  componentId: string,
  animPlan: ComponentAnimationPlan,
  localState: ArtboardLocalState,
): void {
  for (const animDef of animPlan.animations) {
    const linearAnim = encodeLinearAnimation(ctx, animDef);
    ctx.objects.push(linearAnim);

    const needsCubicInterpolator = animDef.tracks.some((track) =>
      track.keyframes.some(
        (keyframe) =>
          (keyframe.interp ?? RiveInterpolationType.Linear) ===
          RiveInterpolationType.Cubic,
      ),
    );

    let cubicInterpolatorObjectId: number | null = null;
    if (needsCubicInterpolator) {
      cubicInterpolatorObjectId = allocateArtboardLocalId(localState);
      const cubicInterpolator = encodeCubicInterpolator(ctx, 0.42, 0, 0.58, 1);
      ctx.objects.push(cubicInterpolator);
    }

    const tracksByBone = new Map<string, (typeof animDef.tracks)[number][]>();
    for (const track of animDef.tracks) {
      if (!tracksByBone.has(track.boneName)) {
        tracksByBone.set(track.boneName, []);
      }
      tracksByBone.get(track.boneName)!.push(track);
    }

    for (const [boneName, tracks] of tracksByBone) {
      const boneObjectId = localState.boneIdMap.get(boneName);

      if (boneObjectId === undefined) {
        ctx.warnings.push(
          `Animation "${animDef.name}": Bone "${boneName}" not found in component ${componentId}`,
        );
        continue;
      }

      const keyedObject = encodeKeyedObject(ctx, boneObjectId);
      ctx.objects.push(keyedObject);

      for (const track of tracks) {
        const rivePropertyKey = getRivePropertyKey(track.property);
        const keyedProp = encodeKeyedProperty(ctx, rivePropertyKey);
        ctx.objects.push(keyedProp);

        for (const kf of track.keyframes) {
          const interpolationType = kf.interp ?? RiveInterpolationType.Linear;
          const interpolatorId =
            interpolationType === RiveInterpolationType.Cubic
              ? cubicInterpolatorObjectId
              : null;
          const keyFrame = encodeKeyFrameDouble(
            ctx,
            kf.frame,
            kf.value,
            interpolationType,
            interpolatorId,
          );
          ctx.objects.push(keyFrame);
        }
      }
    }
  }
}

/**
 * Map animation property names to Rive property keys.
 */
function getRivePropertyKey(prop: string): number {
  switch (prop) {
    case 'x':
      return RivePropertyKey.node_x.key;
    case 'y':
      return RivePropertyKey.node_y.key;
    case 'rotation':
      return RivePropertyKey.rotation.key;
    case 'scaleX':
      return RivePropertyKey.scaleX.key;
    case 'scaleY':
      return RivePropertyKey.scaleY.key;
    default:
      throw new Error(`Unknown animation property: ${prop}`);
  }
}

// ---------------------------------------------------------------------------
// ToC (Table of Contents) generation
// ---------------------------------------------------------------------------

function collectPropertyKeys(objects: EncodedObject[]): Set<number> {
  const keys = new Set<number>();
  for (const obj of objects) {
    for (const key of obj.propertyKeys) {
      keys.add(key);
    }
  }
  return keys;
}

function writeToC(w: BinaryWriter, objects: EncodedObject[]): void {
  const propertyKeys = Array.from(collectPropertyKeys(objects)).sort(
    (a, b) => a - b,
  );

  for (const propertyKey of propertyKeys) {
    w.writeVarint(propertyKey);
  }
  w.writeVarint(0);

  for (let groupStart = 0; groupStart < propertyKeys.length; groupStart += 4) {
    let packedWord = 0;
    for (let offset = 0; offset < 4; offset += 1) {
      const propertyKey = propertyKeys[groupStart + offset];
      if (propertyKey === undefined) {
        continue;
      }

      const def = PROPERTY_BY_KEY.get(propertyKey);
      if (!def) {
        throw new Error(
          `Unknown property key ${propertyKey} while writing ToC.`,
        );
      }

      packedWord |= def.backingType << (offset * 2);
    }
    w.writeUint32(packedWord);
  }
}

// ---------------------------------------------------------------------------
// Object stream writing
// ---------------------------------------------------------------------------

function writeObjectStream(w: BinaryWriter, objects: EncodedObject[]): void {
  for (const obj of objects) {
    // Write type key
    w.writeVarint(obj.typeKey);
    // Write object body
    w.writeBytes(obj.bytes);
    // Write terminator
    w.writeByte(0x00);
  }
}

// ---------------------------------------------------------------------------
// Internal helpers — counting
