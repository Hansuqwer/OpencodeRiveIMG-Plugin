/**
 * Write a `.riv` binary file from the pipeline intermediate representation.
 *
 * Phase 3 behaviour:
 *   Writes a complete object graph with animations:
 *     - Backboard (root)
 *     - For each component:
 *       - Artboard
 *       - Image (child of Artboard)
 *       - Mesh + MeshVertices (if mesh data exists)
 *       - Skin (child of Mesh)
 *       - Bones (RootBone + regular Bone hierarchy)
 *       - LinearAnimations (one per animation plan)
 *         - KeyedObjects (one per animated bone)
 *           - KeyedProperties (one per animated property)
 *             - KeyFrameDoubles (one per keyframe)
 *
 *   The ToC is built in two passes:
 *     1. First pass — encode all objects, collecting property keys used
 *     2. Second pass — write ToC with backing types, then object stream
 *
 *   This output is verified by the Gate 2 runtime-load harness in
 *   `pipeline.test.ts` when `RUN_RIVE_GATE2_EXPECT_PASS=1` is enabled.
 *   Full `.riv` product support is still gated by the remaining checklist in
 *   `HANDOVER.md`.
 *
 * @param input  Immutable pipeline IR from segmentation, pose, and animation stages.
 * @returns      Encoded bytes, artboard/bone counts, and any encoding warnings.
 */
export function writeRiveFile(input: RiveWriterInput): RiveWriterResult {
  const warnings: string[] = [];

  // Initialize encoding context
  const ctx: EncodingContext = {
    objects: [],
    warnings,
    nextId: 0,
  };

  // 1. Create Backboard (always id 0)
  ctx.objects.push(encodeBackboard(ctx));

  // 2. Create a map of pose components by id for quick lookup
  const poseComponentMap = new Map<string, PoseComponent>();
  for (const pc of input.poseResult.components) {
    poseComponentMap.set(pc.id, pc);
  }
  // 3. Create a map of animation plans by component id
  const animPlanMap = new Map<string, (typeof input.animationPlans)[number]>();
  for (const ap of input.animationPlans) {
    animPlanMap.set(ap.id, ap);
  }

  // 4. Encode each segmentation component
  for (const segComponent of input.segResult.components) {
    const poseComponent = poseComponentMap.get(segComponent.id);
    const animPlan = animPlanMap.get(segComponent.id);

    // Determine artboard dimensions
    const width = input.artboardWidth ?? segComponent.image_size.w;
    const height = input.artboardHeight ?? segComponent.image_size.h;

    encodeComponent(ctx, segComponent, poseComponent, width, height, animPlan);
  }

  // 5. Build the file
  const w = new BinaryWriter();

  // Write header
  writeFileHeader(w);

  // Write ToC (built from collected property keys)
  writeToC(w, ctx.objects);

  // Write object stream
  writeObjectStream(w, ctx.objects);

  const artboardCount = input.segResult.components.length;
  const boneCount = countTotalBones(input.poseResult);

  return {
    bytes: w.toUint8Array(),
    artboardCount,
    boneCount,
    warnings,
  };
}
