/**
 * Pipeline IR input consumed by the `.riv` writer.
 *
 * All fields mirror the pipeline stage output types and are intentionally
 * read-only to prevent accidental mutation of shared pipeline state.
 * The writer must NOT modify any of these objects.
 */
export interface RiveWriterInput {
  /** Segmentation output from the run-segmentation stage. */
  readonly segResult: SegResult;
  /** Pose estimation output from the run-pose stage. */
  readonly poseResult: PoseResult;
  /** Animation plans from the generate-animation stage. */
  readonly animationPlans: ReadonlyArray<ComponentAnimationPlan>;
  /**
   * Optional artboard width override in pixels.
   * When omitted, per-component dimensions from segResult are used.
   * Max: 8192 (MAX_ARTBOARD_DIMENSION).
   */
  readonly artboardWidth?: number;
  /**
   * Optional artboard height override in pixels.
   * When omitted, per-component dimensions from segResult are used.
   * Max: 8192 (MAX_ARTBOARD_DIMENSION).
   */
  readonly artboardHeight?: number;
}

/**
 * Result returned by writeRiveFile().
 *
 * The `bytes` field contains the encoded content. In Phase 2 this is a
 * complete static object graph that should be loadable by the Rive runtime,
 * though it has not yet been verified by an automated harness (Gate 2).
 */
export interface RiveWriterResult {
  /**
   * Encoded binary content.
   * Phase 1: header stub only (not loadable).
   * Phase 2+: full static object graph (theoretically loadable).
   * Phase 3+: includes animation data.
   */
  readonly bytes: Uint8Array;
  /**
   * Number of artboards derived from the input segmentation result.
   * Matches input.segResult.components.length.
   */
  readonly artboardCount: number;
  /**
   * Total bone count across all pose components.
   * Computed by summing bones from every PoseComponent.skeleton.bones.
   */
  readonly boneCount: number;
  /**
   * Non-fatal warnings generated during encoding.
   * Phase 1: contained stub notice.
   * Phase 2+: populated on schema coverage gaps or unsupported features.
   */
  readonly warnings: readonly string[];
}

